#include <iostream>
using namespace std;

int main(){

    //Assignment operator (=)
    int x = 10; // storing 10 into x
    cout << "After assignment, x =" << x << endl;

    //Equality operator (==)
    if (x == 10){
        cout << "x is equal to 10 _ this is true!" << endl;
    } else {
        cout << "x is not equal to 10 _ this is false!" << endl;
    }

    return 0;
}